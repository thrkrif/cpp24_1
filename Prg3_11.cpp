/*
    *표현식 내부에서의 명시적 자료형 변환과 암묵적 자료형 변환 비교
*/
#include <iostream>
using namespace std;

int main(){
    //선언
    double x = 23.56;
    int y = 30;
    
    //암묵적 자료형 변환 활용
    cout << "캐스팅을 하지 않는 경우: " << x + y << endl;
    //명시적 자료형 변환
    cout << "캐스팅을 한 경우: " << static_cast<int>(x) + y << endl;

}